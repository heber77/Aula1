class Topicos:
    def __init__(self, nomeTopico, conteudoTopico):
        self.nomeTopico = nomeTopico
        self.conteudoTopico = conteudoTopico

    def criarTopico(self, nomeTopico, conteudoTopico):
        self.nomeTopico = nomeTopico
        self.conteudoTopico = conteudoTopico

    def gerenciarTopico(self, nomeTopico, conteudoTopico):
        self.nomeTopico = nomeTopico
        self.conteudoTopico = conteudoTopico

